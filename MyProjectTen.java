package myprojectten;

public class MyProjectTen {
    public static void main(String[] args) {
        for (int a = 1; a <= 30; a++) {
            if (a % 2 == 0) {
                System.out.println(a + " is even");
            } else {
                System.out.println(a  + " is odd");
            }
    }


    }
}
